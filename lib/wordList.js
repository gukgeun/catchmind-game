export const wordsByDifficulty = {
  easy: easyWords,
  normal: normalWords,
  hard: hardWords,
};

export const allWords = [...easyWords, ...normalWords, ...hardWords];

export function getRandomWord(difficulty) {
  const pool = difficulty && wordsByDifficulty[difficulty] ? wordsByDifficulty[difficulty] : allWords;
  return pool[Math.floor(Math.random() * pool.length)];
}

// picks uniformly at random from the entire pool (no per-difficulty weighting),
// and reports which tier the picked word belongs to for display purposes
export function getRandomWordWithDifficulty() {
  const idx = Math.floor(Math.random() * allWords.length);
  const word = allWords[idx];
  let difficulty;
  if (idx < easyWords.length) difficulty = "easy";
  else if (idx < easyWords.length + normalWords.length) difficulty = "normal";
  else difficulty = "hard";
  return { word, difficulty };
}
